// doubles are decimal number in Dart

void main() {
  double num1 = 20.5;
  double num2 = 10.3;

  double sum = num1 + num2;
  double difference = num1 - num2;
  double multiplication = num1 * num2;

  print(num1);
  print(num2);
  print(sum);
  print(difference);
  print(multiplication);

  //working with int and double
  int num3 = 5;
  double num4 = 2.7;

  final output = num3 - num4;
  print(output);
}